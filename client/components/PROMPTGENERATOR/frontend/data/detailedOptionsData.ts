// Export a function to find a specific category by name
export function findCategoryByName(name: string): CategoryOption | undefined {
  return DETAILED_OPTIONS_CATEGORIES.find((cat) => cat.name === name);
}

// Export a function to find a subcategory within a category
export function findSubCategoryByName(
  categoryName: string,
  subCategoryName: string,
): SubCategoryOption | undefined {
  const category = findCategoryByName(categoryName);
  if (!category) return undefined;
  return category.subCategories.find(
    (subCat) => subCat.name === subCategoryName,
  );
}
